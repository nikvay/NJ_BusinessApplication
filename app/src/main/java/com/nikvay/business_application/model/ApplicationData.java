package com.nikvay.business_application.model;

public class ApplicationData {
    String splash_image;
    String screen_image;
    String application_name;
    String message;

    public String getSplash_image() {
        return splash_image;
    }

    public void setSplash_image(String splash_image) {
        this.splash_image = splash_image;
    }

    public String getScreen_image() {
        return screen_image;
    }

    public void setScreen_image(String screen_image) {
        this.screen_image = screen_image;
    }

    public String getApplication_name() {
        return application_name;
    }

    public void setApplication_name(String application_name) {
        this.application_name = application_name;
    }

    public String getMessage() {
        return message;
    }

    public void setMessage(String message) {
        this.message = message;
    }


}
